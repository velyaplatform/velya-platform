/**
 * Centralized fixture for Brazilian medical specialties (CFM Res. 2.380/2024).
 * Lists the 55 official specialties + key áreas de atuação, each with the
 * procedures, conditions, exams, and ANVISA/CFM regulations the specialty
 * commonly handles.
 *
 * Source of truth enforced by scripts/check-ui-duplications.ts.
 * Do not duplicate these IDs in page files.
 */

export type SpecialtyCategory =
  | 'clinica'
  | 'cirurgica'
  | 'pediatrica'
  | 'diagnostica'
  | 'apoio'
  | 'critica'
  | 'mental'
  | 'reabilitacao';

export interface MedicalSpecialty {
  id: string;            // e.g. 'cardiologia'
  name: string;          // 'Cardiologia'
  cfmCode: string;       // CFM specialty code
  category: SpecialtyCategory;
  council: 'CRM';
  /** Pre-requisite years of residência médica */
  residencyYears: number;
  /** Description in PT-BR */
  description: string;
  /** Common conditions treated (ICD-10 codes) */
  commonConditions: { code: string; name: string }[];
  /** Common procedures (TUSS codes when known) */
  commonProcedures: { code?: string; name: string }[];
  /** Common exams ordered (LOINC or SNOMED when known) */
  commonExams: { code?: string; name: string }[];
  /** Hospital wards/units where this specialty typically works */
  typicalWards: string[];
  /** Áreas de atuação que dependem desta especialidade */
  areasDeAtuacao: string[];
  /** Regulações brasileiras relevantes (ANVISA, CFM, ANS) */
  regulatoryBasis: string[];
}

