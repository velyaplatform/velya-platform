function buildTimeline(state: MedStatus, base: string): TimelineStep[] {
  const order: Record<string, number> = {
    prescrita: 1,
    'em-validacao': 2,
    validada: 3,
    'em-dispensacao': 4,
    'em-preparo': 4,
    'em-transporte': 5,
    'aguardando-admin': 6,
    administrada: 7,
    atrasada: 6,
    'nao-administrada': 6,
  };
  const cutoff = order[state] ?? 0;
  return STEP_TEMPLATE_INTERNAL.map((s, idx) => ({
    key: s.key,
    label: s.label,
    time: idx + 1 <= cutoff ? `${base.slice(0, 2)}:${String((idx + 1) * 7).padStart(2, '0')}` : null,
  }));
}

